						<p>Barangay Violeta was once a purok-6 of Barangay Linabo. As a purok, it was called Sawaga. It can hardly avail its share from the barangay funds to finance infrastructure projects and other needed for its improvements. Mr. Tiburcio Ando, who was then the treasurer of Barangay Linabo, was one of the authors and primary movers for the conversion of Sitio Sawaga into a Barangay. The people overwhelmingly supported the proposed conversion. They signed a petition and submitted it to the Office of the Department of Interior and Local Government. Many politicians and influential people helped in the realization of their dream and one of them was Hon. Wilfredo M. Bides of the Department of Interior and Local Government office of Malaybalay who helped accomplish the pertinent documents and other necessary requirements. It was former Congresswoman Violeta T. Labaria who was the instrument in bringing the documents to Manila.

                            Finally, on December 8, 1988, the conversion was approved and the new barangay was called “Violeta” in gratitude to Congresswoman Violeta Tabios Labaria. Immediately after the approval of conversion, Mr. Federico Cabrejas was appointed as First Barangay Captain of Barangay Violeta. He donated 10,000 sq. meters, a parcel of his land, to the Barangay Government where the barangay office and other government structure are now located.

                            In 1991, Mr. Leonico Z. Adecer was elected as the new Barangay Captain and continued the good works of Mr. Federico Cabrejas. The sudden death of the Punong Barangay Leonico Adecer in 1991 put the reins of running the barangay affairs to the first Kagawad Ariel C. Ando. In the election 2002, Mr. Ariel C. Ando ran for his first crack at the leadership and won the election. And in 2007, Punong Barangay Ariel C. Ando was re-elected as his second term.

                            In the year 2010 election, Mr. Roberto Sr. S. Tumangday defeated Punong Barangay Ariel C. Ando in the Barangay Election as Barangay Chairman. On October 28, 2013 another local election comes, and Punong Barangay Roberto Sr. S. Tumangday remains undefeated for his second term. On May 15, 2017 another local election comes but still Punong Barangay Roberto Sr. S. Tumangday remains undefeated in his third term and he continued the previous and present plan and programs for the development of the barangay.

                            Barangay Violeta is now a fast developing community in the City of Malaybalay. More and more projects were initiated and spearheaded by the barangay government through the help and financial assistance of the City and Provincial Government. As a barangay, it can now appropriate and even mobilize funds needed to improve infrastructure and deliver social services to the local residents. </p>
                                                    
                        <p>
                        The household population of Violeta in the 2015 Census was 2,199 broken down into 510 households or an average of 4.31 members per household.
                        According to the 2015 Census, the age group with the highest population in Violeta is 5 to 9, with 246 individuals. Conversely, the age group with the lowest population is 80 and over, with 17 individuals.
                        Combining age groups together, those aged 14 and below, consisting of the young dependent population which include infants/babies, children and young adolescents/teenagers, make up an aggregate of 34.15% (751). Those aged 15 up to 64, roughly, the economically active population and actual or potential members of the work force, constitute a total of 61.16% (1,345). Finally, old dependent population consisting of the senior citizens, those aged 65 and over, total 4.68% (103) in all.

